<script lang="ts">
/* eslint-disable sonarjs/no-use-of-empty-return-value */
import Fa from 'svelte-fa';
import type { IconDefinition } from '@fortawesome/free-solid-svg-icons';
import { faClose } from '@fortawesome/free-solid-svg-icons/faClose';

interface Props {
  selected: boolean;
  url: string;
  title: string;
  onClose?: () => void;
  icon?: IconDefinition;
}

let { url, selected, title, onClose, icon }: Props = $props();

interface BtnProps {
  icon: IconDefinition;
  title: string;
  onclick: () => void;
}
</script>

{#snippet btn({ icon, title, onclick }: BtnProps)}
  <button
    role="tab"
    class="cursor-pointer hover:bg-[var(--pd-link-hover-bg)] w-4 h-4 rounded-full items-center justify-center flex"
    title={title}
    onclick={onclick}>
    <Fa size="sm" icon={icon} />
  </button>
{/snippet}

<div
  role="tablist"
  class="border-b-[3px] whitespace-nowrap hover:cursor-pointer focus:outline-[var(--pd-tab-highlight)] px-4 flex gap-x-2 items-center"
  class:border-[var(--pd-tab-highlight)]={selected}
  class:border-transparent={!selected}
  class:hover:border-[var(--pd-tab-hover)]={!selected}>
  <a
    href={url}
    class="text-[var(--pd-tab-text)] no-underline"
    class:text-[var(--pd-tab-text-highlight)]={selected}
    aria-controls="open-tabs-list-{title.toLowerCase()}-panel"
    id="open-tabs-list-{title.toLowerCase()}-link">
    {title}
  </a>
  {#if icon}
    <Fa size="sm" icon={icon} />
  {/if}
  {#if onClose}
    {@render btn({ icon: faClose, title: 'Close', onclick: onClose })}
  {/if}
</div>
